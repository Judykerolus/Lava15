package class3;

public class IfElse_10 {
    public static void main(String[] args) {

        boolean hungry=true;
        if(!hungry){ // when we use ! it changes the condition from to true to false and the opposite
            System.out.println("let's eat"); // = no print out
        }
        if(hungry){
            System.out.println("let's eat"); // = let's eat ** this ! works with primitive and non-primitive
        }
    }}
