package class3;

public class OperatorDemo_3 {
    public static void main(String[] args) {
        int number=10;
        System.out.println(number/3); // =3  we use to find the answer for the divide without the reminder
        // ex 10/3 = 3 no reminder
        System.out.println(number%3); // =1 we use this to find out when we try to divide number to get the reminder
        // ex. 10/3 = 3.33 (3 and reminder 1)
        System.out.println();

    }
}
